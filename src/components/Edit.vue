<script setup>
// TODO: 编辑
import { ref, defineExpose, defineEmits } from 'vue'
import axios from 'axios'
// 弹框开关
const dialogVisible = ref(false)

// 准备form：
const form = ref({
  name: '',
  place: '',
  id: '',
})

// 打开开关的函数
const dialogVisibleStatusChange = (row) => {
  //
  console.log('对应数据的一行:', row)
  form.value.name = row.name
  form.value.place = row.place
  form.value.id = row.id
  //

  dialogVisible.value = true
}

defineExpose({
  // dialogVisible,
  dialogVisibleStatusChange,
})

// 孩子使用父亲的函数
const emit = defineEmits(['on-update'])

const onUpdate = async () => {
  // 收集表单数据，调用接口
  await axios.patch(`/edit/${form.value.id}` ,{
      name: form.value.name,
      place: form.value.place,
    })
   
  // 关闭弹框
  dialogVisible.value = false
  // 通知父组件进行列表更新
  emit('on-update')
}
</script>

<template>
  <el-dialog v-model="dialogVisible" title="编辑" width="400px">
    <el-form label-width="50px">
      <el-form-item label="姓名">
        <el-input placeholder="请输入姓名" v-model="form.name" />
      </el-form-item>
      <el-form-item label="籍贯">
        <el-input placeholder="请输入籍贯" v-model="form.place" />
      </el-form-item>
    </el-form>
    <template #footer>
      <span class="dialog-footer">
        <el-button @click="dialogVisible = false">取消</el-button>
        <el-button type="primary" @click="onUpdate()">确认</el-button>
      </span>
    </template>
  </el-dialog>
</template>

<style scoped>
.el-input {
  width: 290px;
}
</style>
